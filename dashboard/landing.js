let locale = "en";
const translatable = [...document.querySelectorAll("[data-i18n]")];
const richTranslatable = [...document.querySelectorAll("[data-i18n-html]")];
const ariaTranslatable = [...document.querySelectorAll("[data-i18n-aria]")];
const langToggle = document.querySelector("#langToggle");
const footerLang = document.querySelector("#footerLang");
const form = document.querySelector("#pilotForm");
const formNote = document.querySelector("#formNote");
const copyright = document.querySelector("#copyright");
const metaDescription = document.querySelector('meta[name="description"]');
const ogTitle = document.querySelector('meta[property="og:title"]');
const ogDescription = document.querySelector('meta[property="og:description"]');

const englishMeta = {
  pageTitle: document.title,
  metaDescription: metaDescription?.content || "",
  ogTitle: ogTitle?.content || "",
  ogDescription: ogDescription?.content || "",
  copyright: `© Wemik ${new Date().getFullYear()}. Built in Qatar.`,
};

function setLocale(nextLocale) {
  locale = nextLocale;
  document.documentElement.lang = locale;
  document.body.dir = locale === "ar" ? "rtl" : "ltr";
  translatable.forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = locale === "ar" ? translations.ar[key] || node.textContent : node.dataset.en || node.textContent;
  });
  richTranslatable.forEach((node) => {
    const key = node.dataset.i18nHtml;
    node.innerHTML = locale === "ar" ? translations.ar[key] || node.innerHTML : node.dataset.enHtml || node.innerHTML;
  });
  ariaTranslatable.forEach((node) => {
    const key = node.dataset.i18nAria;
    node.setAttribute("aria-label", locale === "ar" ? translations.ar[key] || node.getAttribute("aria-label") : node.dataset.enAria || node.getAttribute("aria-label"));
  });
  document.title = locale === "ar" ? translations.ar.pageTitle : englishMeta.pageTitle;
  if (metaDescription) metaDescription.content = locale === "ar" ? translations.ar.metaDescription : englishMeta.metaDescription;
  if (ogTitle) ogTitle.content = locale === "ar" ? translations.ar.ogTitle : englishMeta.ogTitle;
  if (ogDescription) ogDescription.content = locale === "ar" ? translations.ar.ogDescription : englishMeta.ogDescription;
  copyright.textContent = locale === "ar"
    ? translations.ar.copyright.replace("{year}", new Date().getFullYear())
    : englishMeta.copyright;
}

translatable.forEach((node) => {
  node.dataset.en = node.textContent;
});
richTranslatable.forEach((node) => {
  node.dataset.enHtml = node.innerHTML;
});
ariaTranslatable.forEach((node) => {
  node.dataset.enAria = node.getAttribute("aria-label") || "";
});

function toggleLocale() {
  setLocale(locale === "en" ? "ar" : "en");
}

langToggle?.addEventListener("click", toggleLocale);
footerLang?.addEventListener("click", toggleLocale);

form?.addEventListener("submit", (event) => {
  event.preventDefault();
  form.reset();
  formNote.textContent = locale === "ar"
    ? "شكرا. سنتواصل معك من بريد ويمك خلال يومي عمل."
    : "Thanks. We will be in touch from a Wemik address within two business days.";
});

copyright.textContent = englishMeta.copyright;
